    },
    // 返回
    back() {
      this.parent.showFlag = true;
      this.parent.addOrUpdateFlag = false;
      this.parent.ruzhuxinxiCrossAddOrUpdateFlag = false;
      this.parent.contentStyleChange();
    },
    fangjiantupianUploadChange(fileUrls) {
